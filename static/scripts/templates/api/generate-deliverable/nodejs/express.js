/**
 * Generate a session ID for metadata tracking
 */
function generateSessionId() {
  return Math.random().toString(36).substr(2, 9) + '-' +
         Math.random().toString(36).substr(2, 9) + '-' +
         Math.random().toString(36).substr(2, 9);
}

